package model;

public class DataField {
	String field;
	String data;
	
	public DataField() {
		super();
	}

	public DataField(String field, String data) {
		super();
		this.field = field;
		this.data = data;
	}

	public String getField() {
		return field;
	}

	public void setField(String field) {
		this.field = field;
	}

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}
	
	
}
